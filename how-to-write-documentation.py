def train(model, dl_train, optimizer, epoch, criterion, log_interval=100, device='cpu'):
    """
    model: model to train
    dl_test: train data
    optimizer: optimization algorithm
    epoch: current epoch
    criterion: loss function
    log_interval: printing interval
    device: device to train on
    """
    model.train()
    correct = 0
    total = 0
    train_loss = 0.
    for batch_idx, (images, labels) in enumerate(dl_train):
        images, labels = images.to(device), labels.to(device)

        # Implementation of training method
        optimizer.zero_grad()
        output = model(images)
        loss = criterion(output, labels)
        loss.backward()
        optimizer.step()

       # Get some statistics
        train_loss += loss.detach().item()
        pred = output.data.max(1, keepdim=True)[1]
        correct += pred.eq(labels.data.view_as(pred)).sum()
        total += args.batch
        train_acc = 100 * correct / total

        if batch_idx % log_interval == 0:
            print(f"Epoch {epoch + 1}/{batch_idx + 1} -> {train_loss / (batch_idx + 1)} --- acc -> {train_acc}%")

