class CNN(nn.Module):
    def __init__(self):
        super(CNN, self).__init__()

        # First Convolution Block
        self.conv1 = nn.Conv2d(in_channels=3, out_channels=16, kernel_size=5, padding=2)
        self.pool = nn.MaxPool2d(kernel_size=2, stride=2)

        # Second Convolution Block
        self.conv2 = nn.Conv2d(in_channels=16, out_channels=32, kernel_size=5, padding=2)
        self.fc1 = nn.Linear(32 * 8 * 8, 256) # Fully Connected layer
        self.dropout = nn.Dropout(p=0.5) # Dropout layer

        # Output layer
        self.fc2 = nn.Linear(256, 10)


    def forward(self, x):

        # For first block
        x = self.conv1(x)
        x = F.relu(x) # ReLU Activation Function is used
        x = self.pool(x)

        # For second block
        x = self.conv2(x)
        x = F.relu(x)
        x = self.pool(x)

        x = x.view(-1, 32 * 8 * 8) # Here we flattening tensor as told in hints.

        # For Fully Connected layers
        x = self.fc1(x)
        x = F.relu(x)
        x = self.dropout(x) # Also added dropout layer as told in hints.
        x = self.fc2(x)
        return x

