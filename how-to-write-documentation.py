# To achive more than 90% accuracy we are going to use new deep CNN model with BatchNormlization. In the new CNN model we add deeper blocks to extract
# Deeper features.

class CNN(nn.Module):
    def __init__(self):
        super(CNN, self).__init__()

        # Input Block
        self.conv1 = nn.Conv2d(in_channels=3, out_channels=64, kernel_size=3, padding=1)
        self.bn1 = nn.BatchNorm2d(64) # Batch Normalization is key for deeper models

        # Block 2
        self.conv2 = nn.Conv2d(in_channels=64, out_channels=128, kernel_size=3, padding=1)
        self.bn2 = nn.BatchNorm2d(128)
        self.pool1 = nn.MaxPool2d(2, 2) # 32x32 -> 16x16

        # Block 3
        self.conv3 = nn.Conv2d(in_channels=128, out_channels=256, kernel_size=3, padding=1)
        self.bn3 = nn.BatchNorm2d(256)

        # Block 4
        self.conv4 = nn.Conv2d(in_channels=256, out_channels=256, kernel_size=3, padding=1)
        self.bn4 = nn.BatchNorm2d(256)
        self.pool2 = nn.MaxPool2d(2, 2) # 16x16 -> 8x8

        # Fully Connected Classifier
        self.fc1 = nn.Linear(256 * 8 * 8, 1024) # Flattened size is 256 channels * 8 height * 8 width = 16384
        self.fc2 = nn.Linear(1024, 512)
        self.fc3 = nn.Linear(512, 10)
        self.dropout = nn.Dropout(0.5) # Dropout layer to tackle overfitting problem

    def forward(self, x):
        # For Input Block
        x = F.relu(self.bn1(self.conv1(x)))

        # For Block 2
        x = F.relu(self.bn2(self.conv2(x)))
        x = self.pool1(x)

        # For Block 3
        x = F.relu(self.bn3(self.conv3(x)))

        # For block 4
        x = F.relu(self.bn4(self.conv4(x)))
        x = self.pool2(x)

        # For classifier
        x = x.view(-1, 256 * 8 * 8) # Flatten layer
        x = self.dropout(F.relu(self.fc1(x)))
        x = self.dropout(F.relu(self.fc2(x)))
        x = self.fc3(x)

        return x

# Improved CNN architecture explanation:

# 1) First Convolution block: in This block it tackes the RGB image as input and converts it into 64 feature maps using a 3×3 convolution kernel.
#    Batch Normalization is applied to stabilize training and ReLU is used as the activation function.

# 2) Second Convolution block: The output from the first block is passed to this block, where the number of feature maps is increased
#    from 64 to 128. Batch Normalization and ReLU are again applied. A max-pooling layer is used to reduce the spatial dimension

# 3) Third Convolution block: This block further increases the depth of the network by expanding the feature maps from 128 to 256.
#    Batch Normalization and ReLU help in learning deeper representations while maintaining stable gradients.

# 4) Fourth Convolution block: This block keeps the number of feature maps at 256 and applies another convolution operation.
#    Batch Normalization and ReLU are used again, followed by max-pooling which reduces the spatial size from 16×16 to 8×8.
#    This block helps the model learn high-level and abstract features.

# 5) Fully Connected layers: The final feature maps are flattened into a 1D vector of size 256 × 8 × 8 = 16384.
#    This vector is passed through two fully connected layers with 1024 and 512 neurons respectively.
#    Dropout is applied to reduce overfitting. The final fully connected layer outputs 10 values.

